package com.yeahmobi.yscheduler.notice;

/**
 * @author Ryan Sun
 */
public enum NoticeStatus {
    SUCCESS("运行成功"), //
    FAIL("运行失败"), //
    TIMEOUT("运行超时"), //
    SKIP("被跳过"), //
    CANCEL("被取消");

    private String value;

    NoticeStatus(String value) {
        this.value = value;
    }

    public String value() {
        return this.value;
    }
}
