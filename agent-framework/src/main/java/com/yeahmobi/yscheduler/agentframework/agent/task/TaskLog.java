package com.yeahmobi.yscheduler.agentframework.agent.task;

/**
 * @author Leo Liang
 */
public class TaskLog {

    private byte[] data;
    private int    length;

    public TaskLog() {
    }

    public TaskLog(byte[] data, int length) {
        this.data = data;
        this.length = length;
    }

    /**
     * @return the data
     */
    public byte[] getData() {
        return this.data;
    }

    /**
     * @param data the data to set
     */
    public void setData(byte[] data) {
        this.data = data;
    }

    /**
     * @return the length
     */
    public int getLength() {
        return this.length;
    }

    /**
     * @param length the length to set
     */
    public void setLength(int length) {
        this.length = length;
    }

}
